<p class="large-text work-intro">This installation for <span class="red">Nokia Push</span> represents the data from a snowboarding run in realtime, showing the minutiae of the riders physical and mental state.</p>

<div class="work-main wide">

<img src="work/images/nokiapush_01.jpg" class="element"/>

<p class="text-three-column small-text">
<span class="red">Open Data</span><br/>
Using <a href="http://processing.org">Processing</a>, data collected by the <a href="http://www.pushsnowboarding.com/">Push Snowboarding</a> project - physical and mental data collected from snowboarders - is parsed and displayed as a projected digital poster, representing each run in real time.
</p>

<p class="text-three-column small-text last">
<span class="red">Digital Poster.</span><br/>
To allow for the project to be shipped and set up by someone with little familiarity with the piece we developed a system to allow each element to be individually positioned, adjusted and saved for easy recall, as well as the option to load the data from multiple rider's runs.
<br/><br/>
With <a href="http://signal-noise.co.uk/">Signal | Noise</a>
</p>

