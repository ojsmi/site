<p class="large-text work-intro">Reading the tone of the ground beneath it, this <span class="red">Musical Suitcase</span> converts it into an audible representation of the terrain it passes.</p>

<div class="work-main wide">

<img src="work/images/suitcase_01.jpg" class="element"/>

<p class="text-three-column small-text">
<span class="red">Noise Music</span><br/>
A noise instrument inside a large found pink suitcase, with a camera in the base reading the light values of the pavement beneath it and an accelerometer (via wiimote) inside, the device outputs white noise and sine waves based on its surroundings, modulated by the motion of the carrier.
<br/><br/>
<span class="red">Collaboration</span><br/>
As part of a two day workshop presided over by <a href="http://www.kitchenbudapest.hu/en">Kitchen Budapest</a> in Hungary we were presented with a brief history of Noise Music before an individual brainstorming session. Following this, we merged ideas and formed  groups to begin production.
</p>

<p class="text-three-column small-text last">
<span class="red">Performance</span><br/>
The project was intended to conclude with a performance, and the perfect setting - an underground station with a striped floor, perfect for playing like a musical score - was found. Unfortunately, after a brief play we were asked to leave, reconvening in the open air with the other workshop participants ( complete with infra-red squeeze boxes, musical books and rubber band / salad spinner devices ) for a final noisy performance.
<br/><br/>
Created with MAX/MSP &amp; a variety of found bits of technology. In collaboration with Jason Drew, Jordan Poulet-Hector and Eva Verhoeven.
</p>

